package com.lng.dto.empAzureDetails;

public class AzureLargeFaceListDto {

	
	private Integer brId;
	
	private String brName;
	
	private Boolean brIsActive;
	
	private String largeFaceListId;
	
	private String name;
	
	private String userData;
	
	public Integer getBrId() {
		return brId;
	}

	public void setBrId(Integer brId) {
		this.brId = brId;
	}

	public String getBrName() {
		return brName;
	}

	public void setBrName(String brName) {
		this.brName = brName;
	}

	public Boolean getBrIsActive() {
		return brIsActive;
	}

	public void setBrIsActive(Boolean brIsActive) {
		this.brIsActive = brIsActive;
	}

	public String getLargeFaceListId() {
		return largeFaceListId;
	}

	public void setLargeFaceListId(String largeFaceListId) {
		this.largeFaceListId = largeFaceListId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUserData() {
		return userData;
	}

	public void setUserData(String userData) {
		this.userData = userData;
	}
	
	
}
